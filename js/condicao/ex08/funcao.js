export function cores(c) {
    let x;

    if (c == 'azul' || c == 'Azul') {
        x = 'Essa cor é uma cor primaria!!'

    }

    else if ( c == 'vermelho' || c == 'Vermelho' ) {
        x = 'Essa cor é uma cor primaria!!'
    }

    else if (c == 'amarelo' || c == 'Amarelo') {
        x = 'Essa cor é uma cor primaria!!'
    }

    else {
        x = 'Essa cor não é primaria'
    }

    return x;
}